// Difference between runtime and logical error

/*

Runtime error → Program breaks
A runtime error occurs while the program is running and stops execution.

Happens during execution,Program crashes or stops,Error message shown in console,Easier to detect

let x = 10;
console.log(y); // y is not defined

------------------------------------------------------------------------------------------


Logical error → Program lies
A logical error occurs when the program runs without crashing but produces incorrect output.


No error message,Program runs normally,Output is wrong,Harder to find


let a = 10;
let b = 5;
console.log(a - b); // should be a + b

*/