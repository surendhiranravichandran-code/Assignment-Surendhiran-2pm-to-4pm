// List types of JavaScript errors

/*

SyntaxError---->Occurs when the JavaScript code has invalid syntax.

if (x == 5 {
    console.log(x);
}


ReferenceError------->Occurs when trying to use a variable that is not declared or out of scope.(Undefined variable)

console.log(total); ---> total is not defined


TypeError----->Occurs when an operation is performed on a wrong data type.

let num = 10;
num(); --> num is not a function


RangeError----->Occurs when a value is outside the allowed range.(Value out of range)


let arr = new Array(-1); ---> invalid array length


URIError------>Occurs when using invalid URI functions.

decodeURI("%");


EvalError (rare / deprecated)------>Occurs with improper use of eval() (mostly not used today).

*/


